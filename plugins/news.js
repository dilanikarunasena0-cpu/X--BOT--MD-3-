// commands/news.js
const { Sparky, isPublic } = require("../lib");
const axios = require("axios");
const config = require("../config");

Sparky({
    name: "news",
    alias: ["පුවත්", "breaking", "n"],
    category: "news",
    fromMe: isPublic,
    desc: "ලෝකයේ අලුත්ම පුවත් ලබා ගැනීම"
}, async ({ client, m, args }) => {
    try {
        const botName = config.BOT_INFO?.split(";")[0] || "X-KADIYA-MD";

        await client.sendMessage(m.jid, { text: "📥 NewsAPI වෙතින් අලුත්ම පුවත් ලබාගනිමින් පවතී..." }, { quoted: m });

        // Global headlines ලබා ගැනීම
        const apiKey = "15d4000cd98b4ec59387a8bbb1bb5372";
        const response = await axios.get(`https://newsapi.org/v2/top-headlines?language=en&apiKey=${apiKey}`, {
            timeout: 10000
        });

        const articles = response.data?.articles;

        if (!articles || articles.length === 0) {
            return await m.reply("❌ පුවත් සෙවීමේදී දෝෂයක් ඇතිවිය.");
        }

        const topNews = articles.slice(0, 5);
        
        let menuStatus = `
╭───────────────◉
│ *📰 ${botName} NEWS HUB*
├───────────────◉
│✨ Global News Service
├───────────────◉\n`;

        topNews.forEach((item, index) => {
            menuStatus += `│ *${index + 1}️⃣ ${item.title.substring(0, 50)}...*\n`;
        });

        menuStatus += `
╰────────────────◉
> ${botName} WhatsApp Bot

*Reply with (1 - 5):*
සම්පූර්ණ පුවත කියවීමට අංකයක් Reply කරන්න. 🧧
`;

        // මෙනු එක යැවීම සහ එහි message ID එක තබා ගැනීම
        const sentMenu = await client.sendMessage(m.jid, {
            image: { url: topNews[0].urlToImage || "https://res.cloudinary.com/dqlh378fb/image/upload/v1780800370/zanta_media_uploads/y2qrw8srsw1v4dsu5wxv.jpg" },
            caption: menuStatus
        }, { quoted: m });

        const menuId = sentMenu.key.id;

        // මැසේජ් එකෙන් එන Text එක නිවැරදිවම ගන්නා ශ්‍රිතය
        const getMessageText = (msg) => {
            if (!msg?.message) return "";
            return (
                msg.message.conversation ||
                msg.message.extendedTextMessage?.text ||
                msg.message.templateButtonReplyMessage?.selectedId ||
                msg.message.buttonsResponseMessage?.selectedButtonId ||
                ""
            ).trim();
        };

        // Reply එක හරියටම පුවත් මෙනු එකට ආපු එකක්ද කියා පරීක්ෂා කරන Filter එක
        const filter = (msg) => {
            if (!msg?.message) return false;
            if (msg.key.remoteJid !== m.jid) return false;
            if (msg.key.fromMe) return false;

            const text = getMessageText(msg);
            const isNumber = ["1", "2", "3", "4", "5"].includes(text);

            // පරිශීලකයා කෙලින්ම අංකය එව්වොත් හෝ මැසේජ් එක quote කරලා එව්වොත් වැඩ කරනවා
            const quotedMsgId = msg.message.extendedTextMessage?.contextInfo?.stanzaId;
            if (quotedMsgId) {
                return isNumber && quotedMsgId === menuId;
            }

            return isNumber;
        };

        // තත්පර 60ක් රැක සිටීම
        const replyMsg = await new Promise((resolve) => {
            const handler = (chatUpdate) => {
                const msg = chatUpdate.messages?.[0];
                if (filter(msg)) {
                    client.ev.off("messages.upsert", handler);
                    resolve(msg);
                }
            };

            client.ev.on("messages.upsert", handler);

            setTimeout(() => {
                client.ev.off("messages.upsert", handler);
                resolve(null);
            }, 60000); 
        });

        if (!replyMsg) return;

        const userReplyText = getMessageText(replyMsg);
        const idx = parseInt(userReplyText) - 1;

        if (isNaN(idx) || idx < 0 || idx >= topNews.length) return;

        const news = topNews[idx];

        const detailedReport = `
📰 *${news.title}*

📌 *විස්තරය:* ${news.description || news.content || "සම්පූර්ණ විස්තරය ලබාගත නොහැක."}

✍️ *මූලාශ්‍රය:* ${news.source?.name || "NewsAPI"}
📅 *ප්‍රකාශිත දිනය:* ${new Date(news.publishedAt).toLocaleDateString()}

🔗 *සම්පූර්ණ පුවත කියවන්න ලින්ක් එක:*
${news.url}

_Generated by ${botName} News Service_
`;

        if (news.urlToImage) {
            await client.sendMessage(m.jid, {
                image: { url: news.urlToImage },
                caption: detailedReport
            }, { quoted: replyMsg });
        } else {
            await client.sendMessage(m.jid, { 
                text: detailedReport 
            }, { quoted: replyMsg });
        }

    } catch (err) {
        await m.reply("❌ පුවත් සේවාව ලබාගැනීමේදී දෝෂයක් ඇතිවිය: " + err.message);
    }
});

